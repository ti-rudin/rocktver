		готовый прийти на помощь, выполняющий благотворительную деятельность в форме безвозмездного выполнения
		работ, оказания услуг. Волонтер понимает, что фестиваль «Рокополчение» не коммерческое мероприятие
		и все денежные средства полученные от продажи билетов и мерча или от спонсоров необходимы для организации
		мероприятия.
	</div>
	<div class="my-4">
